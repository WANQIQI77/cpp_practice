#include<cstdio>
#include<cstring>
const  int maxn = 100000+5; 
int last, cur, next[maxn];
char s[maxn];
int main(){
	while(scanf("%s",s+1) ==1 ){
		int n=strlen(s+1);//s[0]不保存，从s[1]开始保存
		last = cur =0;
		next[0]=0;
		
		for(int i=0;i<=n;i++){
			char ch =s[i];
			if(ch == '[') cur = 0;
			else if(ch == ']') cur = last;
			//如果是其他字符，则将当前字符插入到 cur 所指向的位置的右边，并更新 cur 和 last。
			else {
				next[i] = next[cur];
				next[cur]=i;
				if(cur ==last) last =i;//更新“最后一个字符编号”
				cur=i; 
	 		}
		} 
		for(int i=next[0];i!=0;i=next[i])
			printf("%c",s[i]);
		printf("\n");
		
	}
	return 0;
}
